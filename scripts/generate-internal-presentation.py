def body(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(11)
    r.font.name = "Calibri"
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.line_spacing = 1.15
